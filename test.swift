import Foundation

// A simple function that adds two integers
func addNumbers(_ a: Int, _ b: Int) -> Int {
    return a + b
}

// Test the function
let number1 = 5
let number2 = 10
let result = addNumbers(number1, number2)

// Print the result
print("The sum of \(number1) and \(number2) is \(result).")
